$(function () {
    $('.notice').delay(3000).fadeOut(5000);

    let $errors = $('.error-message');
    if ($errors.find('li').length > 0) {
        $errors.fadeIn();
    }

    if (typeof FormValidator === 'function') {
        let formValidator = new FormValidator($('form'));
        formValidator.start();
    }
});

